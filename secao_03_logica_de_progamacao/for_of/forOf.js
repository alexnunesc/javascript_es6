// For Of - Estrutura de repetição moderna

// Só funciona com iteráveis (array, string)

// For Of - Strings
const nome = "Lucas";
for (let valor of nome) {
    console.log(valor);
}

// For Of - Arrays
const nomes = ['Lucas', 'João', 'Maria'];

for (let valor of nomes) {
    console.log(valor);
}
